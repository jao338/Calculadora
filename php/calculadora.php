<?php

    class calculadora{

        function somar($a, $b){

            $c = ($a + $b);

            return $c;
        }

        function subtrair($a, $b){

            $c = ($a - $b);

            return $c;
        }

        function multiplicar($a, $b){

            $c = ($a * $b);

            return $c;
        }

        function dividir($a, $b){

            $c = ($a / $b);

            return $c;
        }
    }

?>